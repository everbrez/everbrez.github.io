/**
 * @param {character[][]} grid
 * @return {number}
 */
var numIslands = function (grid) {
  let count = 0
  let queue = []
  for (let i = 0; i < grid.length; i++) {
    for (let j = 0; j < grid[i].length; j++) {
      if (grid[i][j] === '1') {
        queue.push([i, j])
        grid[i][j] = '#'
        while (queue.length > 0) {
          if(queue.length > 500) return;
          let [i, j] = queue.shift()
          if (grid[i + 1] && (grid[i + 1][j] === '1')) {
            queue.push([i + 1, j])
            grid[i + 1][j] = '#'
          }
          if (grid[i][j + 1] && (grid[i][j + 1] === '1')) {
            queue.push([i, j + 1])
            grid[i][j + 1] = '#'
          }
          if (grid[i - 1] && (grid[i - 1][j] === '1')) {
            queue.push([i - 1, j])
            grid[i - 1][j] = '#'
          }
          if (grid[i][j - 1] && (grid[i][j - 1] === '1')) {
            queue.push([i, j - 1])
            grid[i][j - 1] = '#'
          }
        }
        count++
      }
    }
  }

  return count
};

